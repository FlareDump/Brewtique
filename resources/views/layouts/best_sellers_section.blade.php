    }

    .fade-in-left.fade-in-out-opposite {
        opacity: 0;
        transform: translateX(40px);
        /* fade out to the right */
    }

    .fade-in-up.fade-in-out-opposite {
        opacity: 0;
        transform: translateY(-40px);
        /* fade out to the top */
    }
