<script>
    // Fade-in and fade-out on scroll for best sellers section (title, tags, grid)
    window.addEventListener('DOMContentLoaded', function() {
        function fadeInOutOppositeOnScroll(el, direction) {
            if (!el) return;

            function onScroll() {
                const rect = el.getBoundingClientRect();
                if (rect.top < window.innerHeight && rect.bottom > 0) {
                    el.classList.add('fade-in-visible');
                    el.classList.remove('fade-in-out-opposite');
                } else {
                    el.classList.remove('fade-in-visible');
                    el.classList.add('fade-in-out-opposite');
                }
            }
            window.addEventListener('scroll', onScroll);
            onScroll();
        }
        fadeInOutOppositeOnScroll(document.getElementById('best-sellers-title'), 'right');
        fadeInOutOppositeOnScroll(document.getElementById('best-sellers-tags'), 'left');
        // For each product card fade-in-up wrapper in the grid
        document.querySelectorAll('#best-sellers-grid > div > .fade-in-up').forEach(function(card) {
            fadeInOutOppositeOnScroll(card, 'up');
        });
    });
</script>
